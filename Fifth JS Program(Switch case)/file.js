"use strict";

//switch statement
let day=prompt("enter the first three later of day:");
switch(day){
    case "sun":
        console.log("sunday");
        break;
    case "mon":
        console.log("monday");
        break;
    case "tue":
        console.log("tuesday");
        break;
    case "wed":
        console.log("wednesday");
        break;
    case "thu":
        console.log("thursday");
        break;
    case "fri":
        console.log("friday");
        break;
    case "sat":
        console.log("saturday");
        break;
    default:
        console.log("invalid option given");
}
